// Basit normalizasyon: diakritikleri kaldırarak eşleşme deneyecek helper.
// color_engine.dart içinde doğrudan city ismi ile lookup yaparken işe yarar.
// (İsterseniz bu helper'ı computeRegionColor içinde kullanacak şekilde de taşıyabilirim.)
String _normalize(String s) {
  return s
      .toLowerCase()
      .trim()
      .replaceAll('ı', 'i')
      .replaceAll('İ', 'i')
      .replaceAll('ş', 's')
      .replaceAll('ç', 'c')
      .replaceAll('ğ', 'g')
      .replaceAll('ö', 'o')
      .replaceAll('ü', 'u')
      .replaceAll(RegExp(r'[^a-z0-9\s]'), '');
}

double strengthFromMap(Map<String, double> map, String city) {
  if (map.containsKey(city)) return map[city]!;
  final norm = _normalize(city);
  for (final k in map.keys) {
    if (_normalize(k) == norm) return map[k]!;
  }
  // alias örneği:
  if (norm == 'afyon') {
    if (map.containsKey('Afyonkarahisar')) return map['Afyonkarahisar']!;
  }
  return 1.0; // default
}
